package com.ericsson.component.aia.sdk.applicationmanager.views;

/**
 * This view is used to list the integration points' URI Arguments.
 *
 */
public class PbaIntegrationPointUriArgsView {
    private String key;
    private String value;

    public String getKey() {
        return key;
    }

    public void setKey(final String key) {
        this.key = key;
    }

    public String getValue() {
        return value;
    }

    public void setValue(final String value) {
        this.value = value;
    }

    @Override
    public String toString() {
        return "PbaIntegrationPointUriArgsView [key=" + key + ", value=" + value + "]";
    }
}
